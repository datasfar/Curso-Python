print(type(10))                     # int
print(type(1.5))                    # float
print(type(1 + 1j))                 # complex
print(type(True))                   # bool
print(type([1, 2, 3]))              # list
print(type({'name':'Annabel'}))     # dictionary
print(type({9.8, 3.14, 2.7}))       # set
print(type((9.8, 3.14, 2.7)))       # tuple

